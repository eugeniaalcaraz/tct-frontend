export const comboChooser = (comboNumber: number) => {
    switch (comboNumber) {
        case 1:
            return "combos1";
        case 2:
            return "combos2";
        case 3:
            return "combos3";
        case 4:
            return "combos4";
        case 5:
            return "combos5";
        default:
            return "combos1";
    }
};
